    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        try 
        {
            Cronometro cronometro = new Cronometro();
            String[] tempo = {null, null};
            tempo = cronometro.lerTXT();
             
            if(((tempo[0]==null)&&(tempo[1]==null)) || ((tempo[0].isEmpty())&&(tempo[1].isEmpty())))
            {
             JOptionPane.showMessageDialog(this,"Arquivo não existe ou não foi gerado corretamente");   
            }else
            {
            
            jTextFieldTempo1.setText(tempo[0]=(tempo[0].substring(tempo[0].indexOf(";")+1)));
            jTextFieldTempo2.setText(tempo[1]=(tempo[1].substring(tempo[1].indexOf(";")+1)));
            }
        } 
        catch (MyException ex) 
        {
            JOptionPane.showMessageDialog(this,ex.getMessage());
        }
}                                                

