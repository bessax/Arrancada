    private void jMenuSelecionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuSelecionarMouseClicked
        // Se a linha for == -1 siginifica que não tem linha selecionada
        if(jTableCompetidor.getSelectedRow() != -1) {
            codigo = Integer.parseInt(String.valueOf(jTableCompetidor.getValueAt(jTableCompetidor.getSelectedRow() ,0)));
            nome = String.valueOf(jTableCompetidor.getValueAt(jTableCompetidor.getSelectedRow(),3));
            this.dispose();
        } else { 
            //Exibe Mensagem caso nenhuma linha esteja selecionada
            JOptionPane.showMessageDialog(null, "Selecione um Competidor na tabela.");
        }
    }//GEN-LAST:event_jMenuSelecionarMouseClicked

