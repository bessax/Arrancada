    private void jTextFieldPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisaKeyPressed

         jLabelPesquisa.setText(teclaPress.KeyPuxada(evt));
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            if(jTextFieldPesquisa.getText().trim().isEmpty()) {
                manipularJtable.puxada(jTablePuxada,"","1");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Código")) {
                manipularJtable.puxada(jTablePuxada,jTextFieldPesquisa.getText(),"2");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Competição")) {
                manipularJtable.puxada(jTablePuxada,jTextFieldPesquisa.getText(),"7");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Categoria")) {
                manipularJtable.puxada(jTablePuxada,jTextFieldPesquisa.getText(),"9");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Competidor")) {
                manipularJtable.puxada(jTablePuxada,jTextFieldPesquisa.getText(),"10");
            }
            
            if(jTablePuxada.getRowCount() > 0) {
                jTablePuxada.requestFocus();
                jTablePuxada.setRowSelectionInterval(0, 0);
            }
        
        }
    }//GEN-LAST:event_jTextFieldPesquisaKeyPressed

