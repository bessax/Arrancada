    private void jMenuSelecionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuSelecionarMouseClicked
        
        if(jTablePuxada.getSelectedRow() != -1) {
            codigo = Integer.parseInt(String.valueOf(jTablePuxada.getValueAt(jTablePuxada.getSelectedRow() ,0)));
            status = String.valueOf(jTablePuxada.getValueAt(jTablePuxada.getSelectedRow(),5));
            dispose();
        } else {
            //Exibe Mensagem caso nenhuma linha esteja selecionada
            JOptionPane.showMessageDialog(null, "Selecione uma Puxada na tabela.");
        }
}//GEN-LAST:event_jMenuSelecionarMouseClicked

