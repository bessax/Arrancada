    private void jTableCompetidorKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableCompetidorKeyPressed
        // TODO add your handling code here:
        if( (evt.getKeyCode() == KeyEvent.VK_F2) ||(evt.getKeyCode() == KeyEvent.VK_F3) ||
                (evt.getKeyCode() == KeyEvent.VK_F4) ||(evt.getKeyCode() == KeyEvent.VK_F5) ||
                (evt.getKeyCode() == KeyEvent.VK_F4) ) {
            jLabelPesquisa.setText(teclaPress.KeyCategoriaPiloto(evt));
            jTextFieldPesquisa.requestFocus();
        }
    }//GEN-LAST:event_jTableCompetidorKeyPressed

    private void jTextFieldPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisaKeyPressed
        // TODO add your handling code here:
        jLabelPesquisa.setText(teclaPress.KeyCompetidor(evt));
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if(jTextFieldPesquisa.getText().trim().isEmpty()) {
                manipularJTable.competidor(jTableCompetidor, "", "0");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Código")) {
                manipularJTable.competidor(jTableCompetidor, jTextFieldPesquisa.getText().toUpperCase(), "1");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Competição")) {
                manipularJTable.competidor(jTableCompetidor, jTextFieldPesquisa.getText().toUpperCase(), "2");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Categoria")) {
                manipularJTable.competidor(jTableCompetidor, jTextFieldPesquisa.getText().toUpperCase(), "3");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Piloto")) {
                manipularJTable.competidor(jTableCompetidor, jTextFieldPesquisa.getText().toUpperCase(), "4");
            } else if(jLabelPesquisa.getText().equals("Pesquisando por Veículo")) {
                manipularJTable.competidor(jTableCompetidor, jTextFieldPesquisa.getText().toUpperCase(), "5");
            }
            
            if(jTableCompetidor.getRowCount() > 0) {
                jTableCompetidor.requestFocus();
                jTableCompetidor.setRowSelectionInterval(0, 0);
            }
        }
    }//GEN-LAST:event_jTextFieldPesquisaKeyPressed

