    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
       if (jTextFieldCompeticaoNome.getText().equals("Competição não encontrada") || jTextFieldCompeticaoNome.getText().equals("")) 
       {
           jTextFieldCompeticao.requestFocus();
           JOptionPane.showMessageDialog(this, "Você deve informar uma Competição válida.", "Competição não encontrada", JOptionPane.INFORMATION_MESSAGE);
           return;
       }
        
       if(jTextFieldCategoriaNome.getText().equals("Categoria não encontrada") || jTextFieldCategoriaNome.getText().equals(""))
       {
           jTextFieldCategoria.requestFocus();
           JOptionPane.showMessageDialog(this, "Você deve informar uma Categoria cadastrada.","Categoria não encontrada",JOptionPane.INFORMATION_MESSAGE);
           return;
       }
        
       if(jTextFieldCompetidor1Nome.getText().equals("Competidor não Encontrado") || jTextFieldCompetidor1Nome.getText().equals("") && !jComboBoxStatus1.getSelectedItem().equals("Sem Competidor"))
       {
           jTextFieldCompetidor1.requestFocus();
           JOptionPane.showMessageDialog(this, "Você deve informar um Competidor cadastrado.","Competidor 1 não encontrado",JOptionPane.INFORMATION_MESSAGE);
           return;
       }
       
       if(jTextFieldCompetidor2Nome.getText().equals("Competidor não Encontrado") || jTextFieldCompetidor2Nome.getText().equals("") && !jComboBoxStatus2.getSelectedItem().equals("Sem Competidor"))
       {
           jTextFieldCompetidor2.requestFocus();
           JOptionPane.showMessageDialog(this, "Você deve informar um Competidor cadastrado.","Competidor 2 não encontrado",JOptionPane.INFORMATION_MESSAGE);
           return;
       }
        
       if(jTextFieldCompetidor1.getText().trim().equals(jTextFieldCompetidor2.getText().trim())) 
       {
           jTextFieldCompetidor1.requestFocus();
           JOptionPane.showMessageDialog(this, "Competidor 1 igual ao Competidor 2.", "Competidores Iguais", JOptionPane.INFORMATION_MESSAGE);
           return;
       }
        
       if(jTextFieldTempo1.getText().equals("Competidor não Encontrado") || jTextFieldTempo1.getText().equals("") && !jComboBoxStatus1.getSelectedItem().equals("Sem Competidor"))
       {
           jButtonAtualizar.requestFocus();
           JOptionPane.showMessageDialog(this, "Tempo do Competidor 1 inválido.","Tempo 1 não encontrado",JOptionPane.INFORMATION_MESSAGE);
           return;
       }
       if(jTextFieldTempo2.getText().equals("Competidor não Encontrado") || jTextFieldTempo2.getText().equals("") && !jComboBoxStatus2.getSelectedItem().equals("Sem Competidor"))
       {
           jButtonAtualizar.requestFocus();
           JOptionPane.showMessageDialog(this, "Tempo do Competidor 2 inválido.","Tempo 2 não encontrado",JOptionPane.INFORMATION_MESSAGE);
           return;
       }
       
       if(!jComboBoxStatus1.getSelectedItem().equals("Sem Competidor"))
       {
            String numPuxada = servico.proximaPuxada(jTextFieldCompeticao.getText(),jTextFieldCategoria.getText(),jTextFieldCompetidor1.getText());
            int x = servico.insertPuxada(String.valueOf(Integer.parseInt(jTextFieldCodigo.getText().trim())), jTextFieldCompeticao.getText().trim(), jTextFieldCategoria.getText().trim(), jTextFieldCompetidor1.getText().trim(), jTextFieldTempo1.getText().trim(), jComboBoxStatus1.getSelectedItem().toString(), numPuxada);
            if(x == 1)
            {
               String numPuxada2 = servico.proximaPuxada(jTextFieldCompeticao.getText(),jTextFieldCategoria.getText(),jTextFieldCompetidor2.getText()); 
               int z = servico.insertPuxada(String.valueOf(Integer.parseInt(jTextFieldCodigo.getText().trim())+1), jTextFieldCompeticao.getText().trim(), jTextFieldCategoria.getText().trim(), jTextFieldCompetidor2.getText().trim(), jTextFieldTempo2.getText().trim(), jComboBoxStatus2.getSelectedItem().toString(), numPuxada2);
               if(z == 1)
               {
                   atualizarjTable();
                   String zeros = "0000"+servico.proximoCodigo("puxada");
                   jTextFieldCodigo.setText(zeros.substring(zeros.length()-4));
                   JOptionPane.showMessageDialog(this, "Puxada cadastrada com sucesso.","Puxada cadastrada com sucesso.",JOptionPane.INFORMATION_MESSAGE);
               }
            }
       }
       else if(!jComboBoxStatus2.getSelectedItem().equals("Sem Competidor"))
       {
           String numPuxada2 = servico.proximaPuxada(jTextFieldCompeticao.getText(),jTextFieldCategoria.getText(),jTextFieldCompetidor2.getText()); 
           int z = servico.insertPuxada(String.valueOf(Integer.parseInt(jTextFieldCodigo.getText().trim())+1), jTextFieldCompeticao.getText().trim(), jTextFieldCategoria.getText().trim(), jTextFieldCompetidor2.getText().trim(), jTextFieldTempo2.getText().trim(), jComboBoxStatus2.getSelectedItem().toString(), numPuxada2);
           if(z == 1)
           {
                atualizarjTable();
                String zeros = "0000"+servico.proximoCodigo("puxada");
                jTextFieldCodigo.setText(zeros.substring(zeros.length()-4));
                JOptionPane.showMessageDialog(this, "Puxada cadastrada com sucesso.","Puxada cadastrada com sucesso.",JOptionPane.INFORMATION_MESSAGE);
           }
       }
       else
       {
           JOptionPane.showMessageDialog(this, "Nenhum Competidor informado.","Informe um Competidor.",JOptionPane.INFORMATION_MESSAGE);
       }
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

