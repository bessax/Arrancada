    private void jTablePilotoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTablePilotoKeyPressed
        if((evt.getKeyCode() == KeyEvent.VK_F2) ||(evt.getKeyCode() == KeyEvent.VK_F3)) 
        {
            jLabelPesquisa.setText(teclaPress.KeyCategoriaPiloto(evt));
            jTextFieldPesquisa.requestFocus();
        }
        else
        {
            if((evt.getKeyCode() == KeyEvent.VK_ENTER))
            {
               // Se a linha for == -1 siginifica que não tem linha selecionada
                if(jTablePiloto.getSelectedRow() != -1) 
                {
                    codigo = Integer.parseInt(String.valueOf(jTablePiloto.getValueAt(jTablePiloto.getSelectedRow() ,0)));
                    dispose();
                } 
                else 
                {
                    //Exibe Mensagem caso nenhuma linha esteja selecionada
                    JOptionPane.showMessageDialog(null, "Selecione um Veículo na tabela.");
                }
            }
        }
    }//GEN-LAST:event_jTablePilotoKeyPressed
    
    private void centralizar(){  
      /* Função para centralizar janela na tela */  
       Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();  
       Dimension frameSize = getSize(); 
       setLocation(new Point((screenSize.width - frameSize.width) / 2,  
                             (screenSize.height - frameSize.width) / 2));  
    }  
    
