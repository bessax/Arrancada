    private void jTextFieldPesquisaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldPesquisaKeyPressed
        // TODO add your handling code here:
        jLabelPesquisa.setText(teclaPress.KeyPiloto(evt));
        if(evt.getKeyCode() == KeyEvent.VK_ENTER) 
        {
            //manipularJtable.selecionarValor(jTableCategoria, jTextFieldPesquisa.getText(), 0);
            if(jTextFieldPesquisa.getText().trim().isEmpty()) 
            {
                manipularJtable.piloto(jTablePiloto, "", "0");
            } 
            else if(jLabelPesquisa.getText().equals("Pesquisando por Código")) 
            {
                manipularJtable.piloto(jTablePiloto, jTextFieldPesquisa.getText(), "1");
            }
            else if(jLabelPesquisa.getText().equals("Pesquisando por Nome")) 
            {
                manipularJtable.piloto(jTablePiloto, jTextFieldPesquisa.getText(), "2");
            }
            else if(jLabelPesquisa.getText().equals("Pesquisando por UF")) 
            {
                manipularJtable.piloto(jTablePiloto, jTextFieldPesquisa.getText(), "3");
            }
            else if(jLabelPesquisa.getText().equals("Pesquisando por Cidade")) 
            {
                manipularJtable.piloto(jTablePiloto, jTextFieldPesquisa.getText(), "4");
            }
            
            if(jTablePiloto.getRowCount() > 0) 
            {
                jTablePiloto.requestFocus();
                jTablePiloto.setRowSelectionInterval(0, 0);
            }
        }
    }//GEN-LAST:event_jTextFieldPesquisaKeyPressed

